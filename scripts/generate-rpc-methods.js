/**
 * Builds lib/rpcMethods.mainnet.js and lib/rpcMethods.testnet.js from
 * lib/docs.mainnet.json and lib/docs.testnet.json (RPC method names only).
 * Run: npm run generate-rpc-methods
 */
"use strict";

const fs = require("fs");
const path = require("path");

function build(network) {
  const docsPath = path.join(
    __dirname,
    "..",
    "lib",
    `docs.${network}.json`
  );
  const outPath = path.join(
    __dirname,
    "..",
    "lib",
    `rpcMethods.${network}.js`
  );

  const docs = JSON.parse(fs.readFileSync(docsPath, "utf8"));
  const keys = Object.keys(docs)
    .filter((k) => k !== "")
    .sort();

  const body = keys
    .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(k)},`)
    .join("\n");

  const file = `/**
 * RPC method name constants (identity map) for ${network}.
 * Generated by scripts/generate-rpc-methods.js from lib/docs.${network}.json — do not edit by hand.
 * SPDX: MIT — same provenance as lib/evrmoreRpc.js — see lib/LICENSE-evrmore-rpc.txt
 */
"use strict";

module.exports.methods = {
${body}
};
`;

  fs.writeFileSync(outPath, file, "utf8");
  console.log("Wrote", outPath, "(" + keys.length + " methods)");
}

build("mainnet");
build("testnet");
