/**
 * Builds lib/rpcMethods.js from lib/docs.json (RPC method names only).
 * Run: npm run generate-rpc-methods
 */
"use strict";

const fs = require("fs");
const path = require("path");

const docsPath = path.join(__dirname, "..", "lib", "docs.json");
const outPath = path.join(__dirname, "..", "lib", "rpcMethods.js");

const docs = JSON.parse(fs.readFileSync(docsPath, "utf8"));
const keys = Object.keys(docs)
  .filter((k) => k !== "")
  .sort();

const body = keys
  .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(k)},`)
  .join("\n");

const file = `/**
 * RPC method name constants (identity map). Generated by scripts/generate-rpc-methods.js from lib/docs.json — do not edit by hand.
 * SPDX: MIT — same provenance as lib/evrmoreRpc.js — see lib/LICENSE-evrmore-rpc.txt
 */
"use strict";

module.exports.methods = {
${body}
};
`;

fs.writeFileSync(outPath, file, "utf8");
console.log("Wrote", outPath, "(" + keys.length + " methods)");
