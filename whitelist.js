function getActiveWhitelist(network) {
  return network === "testnet" ? whitelistTestnet : whitelistMainnet;
}

function isWhitelisted(method, network) {
  const net =
    network !== undefined && network !== null
      ? network
      : require("./getConfig")().network;
  return getActiveWhitelist(net).includes(method);
}

